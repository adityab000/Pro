    class BackgroundDesktop extends javax.swing.JDesktopPane {
    @Override
    protected void paintComponent(java.awt.Graphics g) {
        super.paintComponent(g);
        java.awt.Graphics2D g2 = (java.awt.Graphics2D) g.create();
        g2.setRenderingHint(java.awt.RenderingHints.KEY_ANTIALIASING,
                            java.awt.RenderingHints.VALUE_ANTIALIAS_ON);

        // ── Dark blue gradient background ────────────────────
        java.awt.GradientPaint gradient = new java.awt.GradientPaint(
            0, 0,                              // top-left
            new java.awt.Color(10, 20, 60),    // dark navy
            getWidth(), getHeight(),            // bottom-right
            new java.awt.Color(30, 58, 138)    // PRIMARY blue
        );
        g2.setPaint(gradient);
        g2.fillRect(0, 0, getWidth(), getHeight());

        // ── Subtle grid lines ────────────────────────────────
        g2.setColor(new java.awt.Color(255, 255, 255, 15));
        g2.setStroke(new java.awt.BasicStroke(1));
        for (int x = 0; x < getWidth(); x += 60) {
            g2.drawLine(x, 0, x, getHeight());
        }
        for (int y = 0; y < getHeight(); y += 60) {
            g2.drawLine(0, y, getWidth(), y);
        }

        // ── Center text watermark ────────────────────────────
        g2.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 48));
        g2.setColor(new java.awt.Color(255, 255, 255, 20));
        String text = "✈  AIRPORT RESERVATION SYSTEM";
        java.awt.FontMetrics fm = g2.getFontMetrics();
        int x = (getWidth() - fm.stringWidth(text)) / 2;
        int y = getHeight() / 2;
        g2.drawString(text, x, y);

        g2.dispose();
    }
}


    

        while (jMenuBar2.getMenuCount() > 0) {
            javax.swing.JMenu menu = jMenuBar2.getMenu(0);
            if (menu != null) {
                menu.setForeground(java.awt.Color.WHITE);
                menu.setFont(AppTheme.FONT_BUTTON);
                menu.setOpaque(false);
                menu.setBorderPainted(false);

                for (int j = 0; j < menu.getItemCount(); j++) {
                    javax.swing.JMenuItem item = menu.getItem(j);
                    if (item == null) continue;
                    item.setFont(AppTheme.FONT_INPUT);
                    item.setBackground(AppTheme.INPUT_BG);
                    item.setForeground(AppTheme.TEXT_DARK);
                    item.setBorder(javax.swing.BorderFactory.createEmptyBorder(6, 12, 6, 12));
                }
                transparentBar.add(menu);
            } else {
                jMenuBar2.remove(0);
            }
        }

        setJMenuBar(transparentBar);
        revalidate();
        repaint();
        
        setLocationRelativeTo(null);
